#include "3-calc.h"

/**
* op_add - adds 2 intgs
* @a: 1st int
* @b: 2nd int
* Return: sum
*/
int op_add(int a, int b)
{
return (a + b);
}

/**
* op_sub - subtracts 2 intgs
* @a: 1st int
* @b: 2nd int
* Return: difference
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
* op_mul - multiplies 2 intgs
* @a: 1st int
* @b: 2nd int
* Return: product
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_div - divides 2 intgs
* @a: 1st int
* @b: 2nd int
* Return: quotient
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
* op_mod - modulo 2 intgs
* @a: 1st int
* @b: 2nd int
* Return: remainder
*/
int op_mod(int a, int b)
{
return (a % b);
}
